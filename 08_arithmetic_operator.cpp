#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 5;

    int sum = a + b;
    int difference = a - b;
    int product = a * b;
    int quotient = a / b;

    cout << "Sum: " << sum << endl;           // Output: Sum: 15
    cout << "Difference: " << difference << endl; // Output: Difference: 5
    cout << "Product: " << product << endl;     // Output: Product: 50
    cout << "Quotient: " << quotient << endl;   // Output: Quotient: 2

    return 0;
}
